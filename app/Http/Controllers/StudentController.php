     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $this->validate($request, [
            'name' => 'required',
            'jurusan' => 'required',
            'kelas' => 'required',
            'nim' => 'required',
        ]);

        // create product
        Student::create([
            'name'  => $request->name,
            'jurusan'  => $request->jurusan,
            'kelas'  => $request->kelas,
            'nim'  => $request->nim,
        ]);

        // redirect
        return redirect()->route('student.index');
    }

